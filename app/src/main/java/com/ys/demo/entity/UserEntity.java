public class UserEntity {
    private int id;
    private String userName;
    private String name;
    private String stuNub;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getStuNub() {
        return stuNub;
    }

    public void setStuNub(String stuNub) {
        this.stuNub = stuNub;
    }
}
